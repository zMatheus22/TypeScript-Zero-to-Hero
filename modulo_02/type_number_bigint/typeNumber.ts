// => Exemplo number

let int: number = 22;
let float: number = 22.22;
let hex: number = 0x78cf;
let oct: number = 0o577;
let bin: number = 0b00101;

console.log("Número inteiro: ", int); // 22
console.log("Número flutuante: ", float); // 22
console.log("Número hexadecimal: ", hex); // 30863
console.log("Número octal: ", oct); // 377
console.log("Número binário: ", bin); // 5
