// => Exemplo de uso de BigInt

let bigInt: bigint = 221072444444449039242304099n;
let bigIntHex: bigint = 0x78cf541234cfffffff231c3n;
let bigIntOct: bigint = 0o512676575555326544263265n;
let bigIntBin: bigint = 0b1100100111000100101010010101n;

console.log("Número inteiro: ", bigInt); // 221072444444449039242304099
console.log("Número hexadecimal: ", bigIntHex); // 2336802864865820784263836099
console.log("Número octal: ", bigIntOct); // 3051760624254495844021
console.log("Número binário: ", bigIntBin); // 211569301
